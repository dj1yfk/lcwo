<h2>Mozilla Firefox</h2>
<p>The plugin <a
href="https://addons.mozilla.org/de/firefox/addon/201/">DownThemAll!</a> 
lets you download any number of links embedded in a site automatically. Just
the right thing to download CW MP3s from LCWO.net. You can
install the plugin directly from the Mozilla site and after restarting your browser it
is available.</p>
<p>Request a set of MP3 files, and access <strong>DownThemAll!</strong> via
the context menu which appears when you right-click somewhere in the browser
window, as shown below (click to enlarge).</p>
<img style="border:1px solid #000000" width=400
onclick="if (this.width == 400) {this.width=498} else {this.width=400;}"
src="/pics/download1.png" alt="[DownThemAll Context menu]" title="Context Menu DownThemAll - click to enlarge">
<p>Now select the custom filter and enter <code>*.mp3, *.txt</code> to download
all the MP3 and Text files to a directory of your choice, as shown below (/tmp/
in this case).</p>
<img width=400
onclick="if(this.width==400){this.width=685}else{this.width=400}"
src="/pics/download2.png" alt="[DownThemAll]" title="DownThemAll - Click to enlarge">
<p>Click <strong>Start!</strong> and lean back while the files are downloaded.
That's it.</p>

