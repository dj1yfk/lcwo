function my_strtoupper ($s) {

	$s = mb_strtoupper($s);

	global $extracharlc;
	foreach ($extracharlc as $c => $k) {
		$s = preg_replace("/$c/", $k, $s);
	}

	/* fix commands like |f, |e and |w from uppercase again*/
	$s = preg_replace("/\|F/", "|f", $s);
	$s = preg_replace("/\|E/", "|e", $s);
	$s = preg_replace("/\|W/", "|w", $s);
	
	return $s;	

}

