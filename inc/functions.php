function stripcommands ($text) {
    $text = preg_replace('/\s+\|[fewFEWST][0-9]+/', ' ', $text);
    $text = preg_replace('/\s+/', ' ', $text);
    $text = preg_replace("/[ ]+/", ' ', $text);
    $text = preg_replace("/^ /", '', $text);
    $text = preg_replace("/ $/", '', $text);	
    $text = preg_replace("/^VVV( )?=( )?/", '', $text);
    $text = preg_replace("/( )?+$/", '', $text);
    return $text;	
}




