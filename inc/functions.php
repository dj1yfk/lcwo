	}
	else if ($c == 'blue') {
		return ('<span style="font-family:monospace;color:#1111ff;">'.$e.'</span>');
	}
	else {
		return ('<span style="font-family:monospace;color:#00aa00;">'.$e.'</span>');
	}
}

