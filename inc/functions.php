/* random group length, 2 to 7, but distributed
 *
 * 2 ==    1 2
 * 3 ===   3 4 5
 * 4 ====  6 7 8 9
 * 5 ====  10 11 12 13
 * 6 ===   14 15 16
 * 7 ==    17 18
 */

function weightedrand1 () {
	$k = intval(rand(1,18));

	if ($k < 3) {
			return 2;
	}	
	elseif ($k < 6) {
			return 3;
	}
	elseif ($k < 10) {
			return 4;
	}
	elseif ($k < 14) {
			return 5;
	}
	elseif ($k < 17) {
			return 6;
	}
	return 7;
}


/* weightedrand2 for letters in the lessons 
 *
 * linear between 0 .. $lesson
 *
 *
 * */

function weightedrand2 ($lesson) {

	if ($lesson <= 9) {
			return intval(rand(0,$lesson));
	}

	$lesson++;

	do {
		$val = sqrt(mt_rand(0, ($lesson*1.5)*($lesson*1.5)));
		$val -= $lesson * 0.5;
	} while ($val < 0);

	$val = intval($val);

    if ($val > ($lesson-1)) {
            $val = ($lesson-1);
    }

	return $val;

}

